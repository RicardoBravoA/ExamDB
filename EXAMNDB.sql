CREATE DATABASE EXAMDB;
GO

USE EXAMDB;
GO

CREATE TABLE TEACHER(
    TEACHER_ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    NAME VARCHAR(30) NOT NULL,
    LAST_NAME VARCHAR(50) NOT NULL
);
GO

CREATE TABLE STUDENT(
    STUDENT_ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    NAME VARCHAR(30) NOT NULL,
    LAST_NAME VARCHAR(50) NOT NULL
);
GO

CREATE TABLE DEGREE(
    DEGREE_ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    DESCRIPTION VARCHAR(30) NOT NULL
);
GO

CREATE TABLE SECTION(
    SECTION_ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    DESCRIPTION VARCHAR(30) NOT NULL
);
GO

CREATE TABLE TEACHERDEGREESECTION(
    TEACHERDEGREESECTION_ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    TEACHER_ID INT FOREIGN KEY REFERENCES TEACHER(TEACHER_ID),
    DEGREE_ID INT FOREIGN KEY REFERENCES DEGREE(DEGREE_ID),
    SECTION_ID INT FOREIGN KEY REFERENCES SECTION(SECTION_ID)
);
GO

CREATE TABLE COURSE(
    COURSE_ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    DESCRIPTION VARCHAR(30) NOT NULL
);
GO

CREATE TABLE TEACHERCOURSE(
    TEACHERCOURSE_ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    TEACHERDEGREESECTION_ID INT FOREIGN KEY REFERENCES TEACHERDEGREESECTION(TEACHERDEGREESECTION_ID),
    COURSE_ID INT FOREIGN KEY REFERENCES COURSE(COURSE_ID)
);
GO

CREATE TABLE ENROLLMENT(
    ENROLLMENT_ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    TEACHERCOURSE_ID INT FOREIGN KEY REFERENCES TEACHERCOURSE(TEACHERCOURSE_ID),
    STUDENT_ID INT FOREIGN KEY REFERENCES STUDENT(STUDENT_ID)
);

CREATE TABLE NOTE(
    NOTE_ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    ENROLLMENT_ID INT FOREIGN KEY REFERENCES ENROLLMENT(ENROLLMENT_ID),
    SCORE INT NOT NULL
);
GO

